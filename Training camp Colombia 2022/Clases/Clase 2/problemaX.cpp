#include <bits/stdc++.h>
using namespace std;

int main(int argc, char const *argv[]) {
    int t; cin >> t;

    while(t--) {
        int n; cin >> n;
        int a[n];
        int sum = 0;
        for(int i=0; i<n; i++) {
            cin >> a[n];
            sum+=a[n];
        }
    }
    return 0;
}
